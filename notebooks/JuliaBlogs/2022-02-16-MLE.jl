md"""

从数学角度来讲， 逻辑回归模型包含以下成分：
- 一个大小为 $n \times (d+1)$ 的设计矩阵常量 $X$。 矩阵每行为一个观测值, 每个观测值有 $d$ 个特征， 以及一个用于确定逻辑回归模型截距的常数项。
- 一个长度为 $n$ 的随机变化但被观测到的数组 $y$。 这些由 0 和 1 二元值组成的数组是逻辑回归模型结果。
- 一个长度为 $d+1$ 的参数常量数组 $\beta$。 这些参数构成了逻辑回归模型的系数。
* 把设计矩阵 $X$ 和系数数组 $\beta$ 相乘可以得到一个数组 $z$。 该数组表示的是对于所有 $i$ 而言， $y_i = 1$ 时的概率。 但该概率是在逻辑空间上的。 要得到概率空间上的概率， 需要用逻辑函数的反函数把 $z_i$ 匹配回 $y_i$： $\text{logit}^{-1}(z) = (1 + \exp(-z))^{-1}$。

上述内容用概率分布符号可以总结如下：

$$X \in \mathbb{R}^{n, d + 1}$$
$$\beta \in \mathbb{R}^{d + 1}$$
$$z = X \beta$$
$$p_i = \text{logit}^{-1}(z_i)$$
$$y_i \sim \text{Bernoulli}(p_i)$$

全部 $n$ 个观测值的似然函数如下

```math
L(\beta) = \prod_{i = 1}^{n} p_i^{y_i} (1 - p_i)^{1 - y_i}
```

更简洁的逻辑化似然函数如下：

```math
\mathcal{L}(\beta) = \sum_{i = 1}^{n} y_i \log(p_i) + (1 - y_i) \log(1 - p_i)
```

我们要通过最大化逻辑似然函数来找到系数 $\beta$。 这等价于最小化负的逻辑似然函数。 
"""

