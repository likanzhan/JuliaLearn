# ╔═╡ 6d5e83c7-d71d-472d-a8f2-9cc7081435ac
md"""
要画一个正态概率图，首先把样本数据从小到大排列。然后计算每个数据点的百分位数。然后假定收 集到的数据服从正态分布，依据每个数据点的百分位数可以求出该数据点对应的修正后分数。以修正后 分数为横坐标，以排序后的原始数据为纵坐标画图。如果原始数据服从正态分布，则该图将沿对角线形成 一条直线。如果不形成一条直线，则说明数据不服从正态分布。
"""

# ╔═╡ d178bda7-4d68-45ff-8184-b7abcc30c709
begin
	mu = 20
	d1, d2 = Normal(mu, mu), Exponential(mu)
	n1 = 100
	data3 = rand(d1, n1)
	data4 = rand(d2, n1)
end;

# ╔═╡ 567844a8-d041-45cd-9633-ec30a374cb62
using Statistics: mean, std

# ╔═╡ ce02311c-4dc2-4169-a72e-017f6a4bdfd0
function qqFun(data)
	m, sd = mean(data), std(data)
	sortData = sort(data)
	dataQuantile = ft.(sortData, Ref(sortData)) # 跟前面联系, 也可用 ecdf
	XValue = quantile(Normal(m, sd), dataQuantile)

	return sortData, XValue
end

# ╔═╡ 6197c72a-ac1e-4e2d-98c6-6c4bfe8c43b5
begin
	sortData1, yStandard1 = qqFun(data3)
	sortData2, yStandard2 = qqFun(data4)
end;

# ╔═╡ 89351789-982e-48d2-a548-d3a5f00f9eef
let
	fig = Figure(resolution = (800, 600))
	ax = Axis(fig[1, 1])
	lines!(ax, yStandard1, yStandard1)
	scatter!(ax, yStandard1, sortData1 , label = "正态分布")
	scatter!(ax, yStandard2, sortData2, label = "指数分布")
	Legend(fig[1, 2], ax, "分布形态")
	fig
end

