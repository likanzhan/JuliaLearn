# ╔═╡ 4a77d1c3-d79d-4f30-a43e-cede3ab7c207
mu2, sigma2 = 40, 12

# ╔═╡ cf5b43a7-c06a-4849-b768-bfc5289508b3
dist1, dist2 = Normal(mu1, sigma1), Normal(mu2, sigma2)

# ╔═╡ a2c3c21f-7853-4709-828b-dd5da1e544ef
p = 0.3

# ╔═╡ 13d977cb-ba65-43ee-aeee-a7caa9510bce
mixRv() = rand() < p ? rand(dist1) : rand(dist2)

# ╔═╡ 5f2b0fee-cee6-432c-9f8f-c49854445929
mixCDF(x) = p * cdf(dist1, x) + (1 - p) * cdf(dist2, x)

# ╔═╡ d9e534cf-6451-4afb-a4c1-fcc32c3fedc2
n = [30, 100]

# ╔═╡ bec5eb09-111a-4b75-8183-9f2afe1d6fab
