# ╔═╡ e6d1f90d-d04d-4d93-b082-aa1ddff0879a
data2 = [mixRv() for _ in 1:n[2]]

# ╔═╡ c6fee81d-485c-4087-945f-68d4f69473d7
md"""
```math
\hat{F}(t) = \frac{1}{n}\sum^n_{i=1}\textbf{1}\{x_i \leq t\}\text{，其中 }\textbf{1}\{x_i \leq t\}\text{ 为示性函数。}
```
"""

# ╔═╡ 489be48f-b7a5-461c-a2e4-0a9fc3d44d6b
ft(t, data) = sum([(x <= t ? 1 : 0) for x in data]) / length(data)

# ╔═╡ f082118f-c867-4f1a-be64-c0c2f66d59aa
